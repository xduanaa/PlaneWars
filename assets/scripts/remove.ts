const {ccclass, property} = cc._decorator;

@ccclass
export default class Remove extends cc.Component {

    @property(cc.Label)
    label: cc.Label = null;

    @property
    text: string = 'hello';

    // LIFE-CYCLE CALLBACKS:

    // onLoad () {}

    start () {
          
    }

    update (dt) {
        for( let fly of this.node.children){
            fly.y-=50*dt
            if(fly.y<=-850){
                fly.y = 830
            }
        }
    }

}
